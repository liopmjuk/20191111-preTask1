DROP TABLE IF EXISTS BIZ_SUPPORT;
DROP TABLE IF EXISTS REGION;

CREATE TABLE BIZ_SUPPORT (
ID INT,
REGION_CODE VARCHAR(200) PRIMARY KEY,
TARGET VARCHAR(1000),
USAGE VARCHAR(200),
BOUND VARCHAR(200),
RATE VARCHAR(200),
INSTITUTE VARCHAR(200),
MGMT VARCHAR(200),
RECEPTION VARCHAR(200),
CREATE_DATE VARCHAR(200),
UPDATE_DATE VARCHAR(200) NULL
);

CREATE TABLE REGION (
  REGION_CODE VARCHAR(50) PRIMARY KEY,
  REGION VARCHAR(200)
) as select * from CSVREAD('classpath:data/지원지자체.csv');